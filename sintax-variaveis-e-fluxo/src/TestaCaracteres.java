
public class TestaCaracteres {
	public static void main(String[] args) {
		char letra = 'a';
		System.out.println(letra);
		
		letra = 66;
		System.out.println(letra);
		
		letra = (char) (letra + 1);
		System.out.println(letra);
		
		String frase = "Curso de Java na Alura";
		System.out.println(frase);
		
		frase = frase + 2022;
		System.out.println(frase);
		
	}
	
}
